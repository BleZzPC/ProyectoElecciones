        if (fila >= 0) {
            txtNombre.setText(tablaPartidos.getValueAt(fila, 0).toString());
            txtSigla.setText(tablaPartidos.getValueAt(fila, 1).toString());
            txtRepresentante.setText(tablaPartidos.getValueAt(fila, 2).toString());
            txtNombre.setEditable(false);
        }
