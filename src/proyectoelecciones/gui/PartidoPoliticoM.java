    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nombre = txtNombre.getText().trim();
        String sigla = txtSigla.getText().trim();
        String representante = txtRepresentante.getText().trim();
        if (nombre.isEmpty() || sigla.isEmpty() || representante.isEmpty()) {
            return;
        }
        if (ca.buscarPartido(nombre) == null) {
            ca.crearPartido(nombre, sigla, representante);
            DefaultTableModel modelo = (DefaultTableModel) tablaPartidos.getModel();
            modelo.setRowCount(0);
            for (proyectoelecciones.Logica.PartidoPolitico p : ca.getPartidos()) {
                modelo.addRow(new Object[]{p.getNombre(), p.getSigla(), p.getRepresentante()});
            }
            txtNombre.setText("");
            txtSigla.setText("");
            txtRepresentante.setText("");
            tablaPartidos.clearSelection();
            txtNombre.setEditable(true);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        int fila = tablaPartidos.getSelectedRow();
        if (fila >= 0) {
            String nombre = txtNombre.getText().trim();
            String sigla = txtSigla.getText().trim();
            String representante = txtRepresentante.getText().trim();
            proyectoelecciones.Logica.PartidoPolitico p = ca.buscarPartido(nombre);
            if (p != null) {
                p.setSigla(sigla);
                p.setRepresentante(representante);
                DefaultTableModel modelo = (DefaultTableModel) tablaPartidos.getModel();
                modelo.setRowCount(0);
                for (proyectoelecciones.Logica.PartidoPolitico partido : ca.getPartidos()) {
                    modelo.addRow(new Object[]{partido.getNombre(), partido.getSigla(), partido.getRepresentante()});
                }
                txtNombre.setText("");
                txtSigla.setText("");
                txtRepresentante.setText("");
                tablaPartidos.clearSelection();
                txtNombre.setEditable(true);
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tablaPartidos.getSelectedRow();
        if (fila >= 0) {
            String nombre = txtNombre.getText().trim();
            ca.eliminarPartido(nombre);
            DefaultTableModel modelo = (DefaultTableModel) tablaPartidos.getModel();
            modelo.setRowCount(0);
            for (proyectoelecciones.Logica.PartidoPolitico p : ca.getPartidos()) {
                modelo.addRow(new Object[]{p.getNombre(), p.getSigla(), p.getRepresentante()});
            }
            txtNombre.setText("");
            txtSigla.setText("");
            txtRepresentante.setText("");
            tablaPartidos.clearSelection();
            txtNombre.setEditable(true);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

